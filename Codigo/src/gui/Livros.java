    final void fillCBCat() throws SQLException {
        CategoriaDAO dao = new CategoriaDAO();
        List<model.Categoria> categoria = dao.list();
        jcbCat.removeAllItems();
        categoria.forEach((p) -> {
            jcbCat.addItem(p.getNome());
        });
    }

    private void loadRecords() throws SQLException {
        String sql = "SELECT isbn, titulo, autor , ano , preco, categoria FROM Livros ORDER BY isbn;";
        ResultSetTableModel tableModel = new ResultSetTableModel(sql);
        jTable1.setModel(tableModel);

        //Adjusting columns 
        jTable1.getColumnModel().getColumn(0).setWidth(200);
        jTable1.getColumnModel().getColumn(0).setMinWidth(50);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(200);

        jTable1.getSelectionModel().addListSelectionListener((ListSelectionEvent event) -> {
            if (jTable1.getSelectedRow() >= 0) {
                Object isbn = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0);
                Object titulo = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1);
                Object autor = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 2);
                Object ano = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 3);
                Object preco = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 4);
                Object categoria = jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 5);
                txtIsbn.setText(isbn.toString());
                txtIsbn.setEnabled(false);
                txtTitulo.setText(titulo.toString());
                txtAutor.setText(autor.toString());
                txtAno.setText(ano.toString());
                txtPreco.setText(preco.toString());

                CategoriaDAO cat = new CategoriaDAO();

                try {
                    jcbCat.setSelectedItem(cat.find(categoria.toString()).getNome());
                } catch (SQLException ex) {
                    Logger.getLogger(Livros.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        });

        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(SwingConstants.LEFT);
        jTable1.getColumnModel().getColumn(0).setCellRenderer(rightRenderer);
    }

    private void clearInputBoxes() {
        txtIsbn.setText("");
        txtTitulo.setText("");
        txtAutor.setText("");
        txtAno.setText("");
        txtPreco.setText("");
    }

    private void addNew() throws SQLException, ParseException {
        LivrosDAO dao = new LivrosDAO();
        model.Livros livros = new model.Livros();
        livros.setIsbn(Integer.parseInt(txtIsbn.getText()));
        livros.setTitulo(txtTitulo.getText());
        livros.setAutor(txtAutor.getText());
        livros.setAno(txtAno.getText());

        livros.setCategoria(jcbCat.getSelectedItem().toString());

        livros.setPreco(Double.parseDouble(txtPreco.getText()));

        dao.insert(livros);
    }

    private void updateRecord() throws SQLException, ParseException {
        LivrosDAO dao = new LivrosDAO();
        model.Livros prod = new model.Livros();
        prod.setIsbn(Integer.parseInt(txtIsbn.getText()));
        prod.setTitulo(txtTitulo.getText());
        prod.setAutor(txtAutor.getText());
        prod.setAno(txtAno.getText());
        prod.setPreco(Integer.parseInt(txtIsbn.getText()));

        CategoriaDAO cat = new CategoriaDAO();
        List<model.Categoria> categoria = cat.list();

        categoria.stream().filter((p) -> (p.getNome().equals(jcbCat.getSelectedItem().toString()))).forEachOrdered((p) -> {
            prod.setCategoria(p.getNome());
        });
        prod.setPreco(Double.parseDouble(txtAno.getText()));
        dao.update(prod);
    }

    private void deleteRecord() throws SQLException {
        LivrosDAO dao = new LivrosDAO();
        dao.remove(Integer.parseInt(txtIsbn.getText()));
    }

